import json
import re

def parse_data(raw):
    years_data = {}
    current_year = None
    
    # Split by double newline or year marker
    lines = raw.strip().splitlines()
    for line in lines:
        line = line.strip()
        if not line:
            continue
        
        # Match HK Year
        match_year = re.match(r"HK (\d{4})", line)
        if match_year:
            current_year = match_year.group(1)
            years_data[current_year] = []
            continue
            
        if line.startswith("SENIN"):
            continue
            
        # Parse data row
        nums = line.split()
        # All nums should be 4 digits (or 2-4 as per prompt logic previously)
        # Pad with XXXX if last week is short
        if current_year:
            # If it's a list of 4 digits numbers
            if all(len(n) <= 4 for n in nums):
                while len(nums) < 7:
                    nums.append("XXXX")
                years_data[current_year].append(nums)
                
    return years_data

